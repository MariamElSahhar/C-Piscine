void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		temp;

	if (size <= 1)
		return ;
	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			temp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = temp;
		}
		i ++;
	}
	ft_sort_int_tab(tab, size - 1);
}
