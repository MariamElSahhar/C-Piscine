#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	int		p;

	if (nb < 0)
	{
		nb = nb * -1;
		write(1, "-", 1);
	}
	if (nb == 0)
		write(1, "0", 1);
	p = 1;
	while ((nb / p) > 1)
	{
		p = p * 10;
	}
	p = p / 10;
	while (p >= 1)
	{
		c = 48 + nb / p;
		write(1, &c, 1);
		nb = nb % p;
		p = p / 10;
	}
}
